		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='huihuamingcheng'){
							this.ruleForm.huihuamingcheng = obj[o];
							this.ro.huihuamingcheng = true;
							continue;
						}
						if(o=='fengmiantupian'){
							this.ruleForm.fengmiantupian = obj[o];
							this.ro.fengmiantupian = true;
							continue;
						}
						if(o=='huihualeixing'){
							this.ruleForm.huihualeixing = obj[o];
							this.ro.huihualeixing = true;
							continue;
						}
						if(o=='xuehao'){
							this.ruleForm.xuehao = obj[o];
							this.ro.xuehao = true;
							continue;
						}
						if(o=='xueshengxingming'){
							this.ruleForm.xueshengxingming = obj[o];
							this.ro.xueshengxingming = true;
							continue;
						}
						if(o=='xiangqing'){
							this.ruleForm.xiangqing = obj[o];
							this.ro.xiangqing = true;
							continue;
						}
						if(o=='fabushijian'){
							this.ruleForm.fabushijian = obj[o];
							this.ro.fabushijian = true;
							continue;
						}
						if(o=='piaoshu'){
							this.ruleForm.piaoshu = obj[o];
							this.ro.piaoshu = true;
							continue;
						}
				}
				








				this.ruleForm.piaoshu='0'


			}
			
			
